#!/bin/bash

# АРХИТЕКТУРНО ПРАВИЛЬНОЕ ФИНАЛЬНОЕ РЕШЕНИЕ
# Реализует принципы современной архитектуры Python-сервисов

echo "🏗️ АРХИТЕКТУРНОЕ ИСПРАВЛЕНИЕ СИСТЕМЫ"
echo "======================================"
echo ""

cd "/Users/verakoroleva/Desktop/@marketing" || exit 1

# Шаг 1: Установка зависимостей для контроллера
echo "1️⃣ Проверка зависимостей..."
if ! python3 -c "import psutil" 2>/dev/null; then
    echo "📦 Установка psutil для контроля процессов..."
    pip3 install psutil
fi

# Шаг 2: Установка исполняемых прав
echo "2️⃣ Настройка прав доступа..."
chmod +x ensure_single_instance.py
chmod +x start_production_bot.py

# Шаг 3: Запуск архитектурного контроллера единственности
echo "3️⃣ Обеспечение единственности экземпляра..."
python3 ensure_single_instance.py

if [ $? -ne 0 ]; then
    echo "❌ КРИТИЧЕСКАЯ ОШИБКА: Контроллер единственности не смог исправить ситуацию"
    echo "💡 Возможные решения:"
    echo "   1. Перезагрузка системы"
    echo "   2. Ручное завершение всех Python процессов: sudo pkill -9 python"
    echo "   3. Проверка блокирующих процессов: ps aux | grep python"
    exit 1
fi

# Шаг 4: Запуск Production бота с полным контролем
echo "4️⃣ Запуск Production бота..."
echo "🎯 Используется архитектурно правильный подход с:"
echo "   • Контролем единственности экземпляра"
echo "   • Graceful shutdown при сигналах"
echo "   • Структурированным логированием"
echo "   • Мониторингом состояния в реальном времени"
echo ""
echo "🚀 ВНИМАНИЕ: Логи будут отображаться в реальном времени"
echo "   Для остановки используйте Ctrl+C (будет выполнен graceful shutdown)"
echo ""
echo "Запуск через 3 секунды..."
sleep 3

# Запуск с полным архитектурным контролем
python3 start_production_bot.py