def _markdown_to_telegram_html(text: str) -> str:
    """–ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ—Ç Markdown-—Ä–∞–∑–º–µ—Ç–∫—É Gemini –≤ HTML –¥–ª—è Telegram."""
    if not text:
        return ""
    
    # –≠–∫—Ä–∞–Ω–∏—Ä—É–µ–º —Å–ø–µ—Ü—Å–∏–º–≤–æ–ª—ã HTML (<, >, &) –ü–ï–†–ï–î –æ–±—Ä–∞–±–æ—Ç–∫–æ–π Markdown
    # –≠—Ç–æ –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ—Ç –æ—à–∏–±–∫–∏ "Can't parse entities", –µ—Å–ª–∏ –≤ —Ç–µ–∫—Å—Ç–µ –µ—Å—Ç—å < –∏–ª–∏ >
    text = html.escape(text, quote=False)

    
    # 1. –ñ–∏—Ä–Ω—ã–π —Ç–µ–∫—Å—Ç: **text** -> <b>text</b>
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º –Ω–µ–∂–∞–¥–Ω—ã–π –ø–æ–∏—Å–∫ .*? —á—Ç–æ–±—ã –Ω–µ –∑–∞—Ö–≤–∞—Ç–∏—Ç—å –≤–µ—Å—å —Ç–µ–∫—Å—Ç
    text = re.sub(r'\*\*(.*?)\*\*', r'<b>\1</b>', text)
    
    # 2. –ñ–∏—Ä–Ω—ã–π —Ç–µ–∫—Å—Ç alternative: __text__ -> <b>text</b>
    text = re.sub(r'__(.*?)__', r'<b>\1</b>', text)
    
    # 3. –ö—É—Ä—Å–∏–≤: *text* -> <i>text</i> (—Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –Ω–µ –≤–Ω—É—Ç—Ä–∏ —Ç–µ–≥–∞)
    # –°–Ω–∞—á–∞–ª–∞ –∑–∞—â–∏—Ç–∏–º —É–∂–µ —Å–æ–∑–¥–∞–Ω–Ω—ã–µ —Ç–µ–≥–∏, –µ—Å–ª–∏ –Ω—É–∂–Ω–æ, –Ω–æ –ø—Ä–æ—Å—Ç–æ–π —Ä–µ–ø–ª–µ–π—Å –æ–±—ã—á–Ω–æ —Ä–∞–±–æ—Ç–∞–µ—Ç, 
    # –µ—Å–ª–∏ –Ω–µ—Ç –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç–∏. Telegram –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç—å <b><i>...</i></b> –∏–¥–µ–∞–ª—å–Ω–æ, –Ω–æ –ø—Ä–æ–±—É–µ–º.
    text = re.sub(r'(?<!\*)\*(?!\*)(.*?)(?<!\*)\*(?!\*)', r'<i>\1</i>', text)
    
    # 4. –°—Å—ã–ª–∫–∏: [Text](URL) -> <a href="URL">Text</a>
    text = re.sub(r'\[([^\]]+)\]\((https?://[^\s)]+)\)', r'<a href="\2">\1</a>', text)
    
    # 5. –ó–∞–≥–æ–ª–æ–≤–∫–∏: ### Header -> <b>Header</b>
    text = re.sub(r'^#{1,6}\s+(.*)$', r'<b>\1</b>', text, flags=re.MULTILINE)
    
    return text


def _convert_markdown_links(text: str) -> str:
    # [–¢–µ–∫—Å—Ç](https://example.com) -> –¢–µ–∫—Å—Ç ‚Äî https://example.com
    return re.sub(r'\[([^\]]+)\]\((https?://[^\s)]+)\)', r'\1 ‚Äî \2', text)


def _format_links(text: str) -> str:
    url_re = re.compile(r'https?://[^\s\)\]\}>]+')
    lines = text.splitlines()
    out_lines = []

    for line in lines:
        stripped = line.strip()
        if not stripped:
            out_lines.append(line)
            continue

        urls = url_re.findall(stripped)
        if not urls:
            out_lines.append(line)
            continue

        # –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ ‚Äî —ç—Ç–æ —Ç–æ–ª—å–∫–æ —Å—Å—ã–ª–∫–∞(–∏)
        if stripped in urls or stripped.rstrip(".,;") in urls:
            if len(urls) == 1:
                out_lines.append(f"–°—Å—ã–ª–∫–∞ ‚Äî {urls[0]}")
            else:
                for u in urls:
                    out_lines.append(f"–°—Å—ã–ª–∫–∞ ‚Äî {u}")
            continue

        # –ò–Ω–∞—á–µ –±–µ—Ä–µ–º –ø–µ—Ä–≤—É—é —Å—Å—ã–ª–∫—É –∏ –¥–µ–ª–∞–µ–º "–¢–µ–∫—Å—Ç ‚Äî URL"
        url = urls[0]
        label = stripped.replace(url, "").strip()
        label = re.sub(r'[:‚Äî‚Äì-]+$', '', label).strip()
        if not label:
            label = "–°—Å—ã–ª–∫–∞"
        out_lines.append(f"{label} ‚Äî {url}")

    return "\n".join(out_lines)


def _strip_markdown(text: str) -> str:
    # –£–±–∏—Ä–∞–µ–º —Ç–æ–ª—å–∫–æ —Å–∏–º–≤–æ–ª—ã, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–≥—É—Ç —Å–ª–æ–º–∞—Ç—å –Ω–µ–∫–æ—Ç–æ—Ä—ã–µ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å—ã 
    # (–Ω–æ –æ—Å—Ç–∞–≤–ª—è–µ–º –∂–∏—Ä–Ω—ã–π, –∫—É—Ä—Å–∏–≤ –∏ –∑–∞–≥–æ–ª–æ–≤–∫–∏ –¥–ª—è –¢–µ–ª–µ–≥—Ä–∞–º–∞)
    text = text.replace("```", "")
    text = text.replace("`", "")
    return text


def _normalize_whitespace(text: str) -> str:
    lines = []
    for line in text.splitlines():
        line = re.sub(r"[ \t]+", " ", line).strip()
        lines.append(line)
    return "\n".join(lines).strip()


def _ensure_emojis(text: str) -> str:
    emoji_re = re.compile(r'[\U0001F300-\U0001FAFF\u2600-\u26FF\u2700-\u27BF]')
    count = len(emoji_re.findall(text))
    if count == 0:
        return f"{text} üôÇ‚ú®"
    if count == 1:
        return f"{text} ‚ú®"
    return text


def mask_phone(phone: str) -> str:
    """–ú–∞—Å–∫–∏—Ä—É–µ—Ç –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞: 89123456789 -> 8*******89."""
    if not phone or not str(phone).strip():
        return "****"
    s = str(phone).strip()
    if len(s) < 4:
        return "****"
    return s[:1] + "*" * (len(s) - 3) + s[-2:]


def mask_telegram_id(tid) -> str:
    """–ú–∞—Å–∫–∏—Ä—É–µ—Ç Telegram ID: 123456789 -> 123***789."""
    if tid is None:
        return "***"
    s = str(tid)
    if len(s) < 6:
        return "***"
    return s[:3] + "***" + s[-3:]


def mask_fio(fio: str) -> str:
    """–ú–∞—Å–∫–∏—Ä—É–µ—Ç –§–ò–û: –ò–≤–∞–Ω–æ–≤ –ò–≤–∞–Ω –ò–≤–∞–Ω–æ–≤–∏—á -> –ò***–≤ –ò***–Ω –ò***—á."""
    if not fio or not str(fio).strip():
        return "***"
    parts = str(fio).strip().split()
    masked = []
    for part in parts:
        if len(part) < 3:
            masked.append("***")
        else:
            masked.append(part[0] + "*" * (len(part) - 2) + part[-1])
    return " ".join(masked)


def _validate_url(url: str) -> bool:
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –≤–∞–ª–∏–¥–Ω—ã–º URL."""
    if not url:
        return False
    try:
        result = urlparse(url)
        return all([result.scheme, result.netloc])
    except Exception:
        return False

def get_web_app_url() -> str:
    """–õ–µ–Ω–∏–≤–æ–µ —á—Ç–µ–Ω–∏–µ URL WebApp –∏–∑ –æ–∫—Ä—É–∂–µ–Ω–∏—è (–ø–æ—Å–ª–µ –∑–∞–≥—Ä—É–∑–∫–∏ .env)."""
    base_url = os.getenv("WEB_APP_URL") or ""
    
    # –í–∞–ª–∏–¥–∞—Ü–∏—è URL
    if base_url and not _validate_url(base_url):
        logger.error(f"WEB_APP_URL —Å–æ–¥–µ—Ä–∂–∏—Ç –Ω–µ–≤–∞–ª–∏–¥–Ω—ã–π URL: {base_url}")
        return ""
    
    if base_url and not base_url.endswith('/'):
        base_url += '/'
    url = base_url + "index.html"
    logger.debug(f"Generated WebApp URL: {url}")
    return url

